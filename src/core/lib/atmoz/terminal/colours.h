#define GREEN2 0x0, 0x80, 0x0
#define BLUE_NICE 0x0, 0x33, 0xAA
#define GRAY_LIGHT 0x66, 0x66, 0x66
#define GRAY_DARK 0x33, 0x33, 0x33
#define NORTON_UTILITIES_VIOLET 0x55, 0x55, 0xFF
